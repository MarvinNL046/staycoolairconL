import React from 'react';

interface FAQItem {
  question: string;
  answer: string;
}

interface FAQSchemaProps {
  faqs: FAQItem[];
}

/**
 * FAQSchema Component
 * Generates JSON-LD structured data for FAQ rich snippets in Google Search
 * This enables the FAQ accordion display in search results
 */
export default function FAQSchema({ faqs }: FAQSchemaProps) {
  if (!faqs || faqs.length === 0) return null;

  const schemaData = {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    mainEntity: faqs.map((faq) => ({
      '@type': 'Question',
      name: faq.question,
      acceptedAnswer: {
        '@type': 'Answer',
        text: faq.answer,
      },
    })),
  };

  return (
    <script
      type="application/ld+json"
      dangerouslySetInnerHTML={{ __html: JSON.stringify(schemaData) }}
    />
  );
}

/**
 * Helper function to create FAQ items from component data
 */
export function createFAQItems(items: { vraag: string; antwoord: string }[]): FAQItem[] {
  return items.map((item) => ({
    question: item.vraag,
    answer: item.antwoord,
  }));
}
